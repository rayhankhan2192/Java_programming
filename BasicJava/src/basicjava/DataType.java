package basicjava;
public class DataType {
    public static void main(String[] srgs){
        short a=20, b=20;
        System.out.println("The value of A = "+a);
        System.out.printf("The value of B = %d\n",b);
        //with using printf we can user formate specifier
        System.out.println("The sum of A + B = "+(a+b));
        System.out.println("The sub of A - B = "+(a-b));
        System.out.println("The multi of A*B = "+(a*b));
        System.out.println("The div of A/B = "+(a/b));
        System.out.println("The modulas of A%B = "+(a%b));
    }
}
