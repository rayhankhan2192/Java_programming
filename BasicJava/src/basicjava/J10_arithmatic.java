package basicjava;

public class J10_arithmatic {
    public static void main(String[] args){
        int num_1 = 10;
        int num_2 = 20;
        int sum = num_1 + num_2;
        System.out.println(sum);
        int sub = num_2 - num_1;
        System.out.println(sub);
        int mult = num_1*num_2;
        System.out.println(mult);
        double div = num_2/num_1;
        System.out.println(div);
    }
    
}
